//0x01抬腿，0x02放下

void legRise(){
	leg_send(0x01);
}

void legDown(){
	leg_send(0x02);
}

void leg_send(u8 msg){
	HAL_UART_Transmit(&UART3_Handler, &msg, 1, 1000);
}
